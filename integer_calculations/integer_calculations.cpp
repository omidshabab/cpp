#include <iostream>

using namespace std;

int main(){
    int m=54;
    int n=20;
    cout << "m = " << m << " and n = " << n << endl;
    cout << "m+n = " << m+n << endl;
    cout << "m-n = " << m-n << endl;
    cout << "m*n = " << m*n << endl;
    cout << "m/n = " << m/n << endl;
    cout << "m%n = " << m%n << endl;
    return 0;
}